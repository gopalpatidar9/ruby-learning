##### attr_reader

# attr_reader automatically generates a getter method for each given attribute.

##### Things to Remember

# • You can only access those instance variables which you have passed to attr_reader.
# • You can only use attr_reader in a class. Using attr_reader outside a class will give you a NoMethodError.


