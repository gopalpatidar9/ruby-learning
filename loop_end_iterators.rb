### loops 

=begin
    
• A loop is the repetitive execution of a chunk of code for a
  given amount of repetitions or until a certain condition is
  met.

• Loop is an older concept and available to use in almost all
  the programming languages.

#### types of loop

 1. while
 2. for
 3. until
    
=end

