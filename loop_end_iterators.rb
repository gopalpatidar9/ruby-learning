#### Iterators

=begin
    
  Iterators are methods that naturally loop over a given set of
  data and allow you to operate on each element in the
  collection.

##### types of iterators

      1. each
      2. each_with_index
      3. map
      4. select
      5. collect
      6. times
    
=end